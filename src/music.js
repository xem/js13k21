coin_sound = i => {
  var n=1.6e4;
  var c=n/7;
  if (i > n) return null;
  var q=Math.pow(t(i,n),2.1);
  return ((i<c ? ((i+Math.sin(-i/900)*10)&16) : i&13) ?q:-q)/9;
}

bzzt_sound = i => {
  var n=1e4;
  if (i > n) return null;
  var q = t(i,n);
  return Math.sin(i/55*Math.sin(i/99)+Math.sin(i/100))*q/5;
}

brrr_sound = i => {
  var n=9e5;
  if (i > n) return null;
  var q = t(i,n);
  return Math.sin(i*0.001*Math.sin(0.001*i+Math.sin(i/100)))*q*q/5;
}

